package com.bootcamp.week01;

public class CastingDemo {

    public static void main(String[] args) {
        int a = 7, b = 2;
        System.out.println("Integer value of a/b = " + (a / b));

        System.out.println("Float value of a/b = " + (float) a / b);
        System.out.println("Double value of a/b = " + (double) a / b);
        System.out.println("Byte value of a + b = " + (byte) a + b);
    }
}
