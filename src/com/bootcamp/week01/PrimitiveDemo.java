package com.bootcamp.week01;

public class PrimitiveDemo {

    public static void main(String[] args) {
        byte byteValue = 1;
        short shortValue = 10;
        int intValue = 50;
        long longValue = 78;
        float floatValue = 5.2f;
        double doubleValue = 5.6;
        char charValue = 't';
        boolean booleanValue = true;

        System.out.println("This is a variable of type Byte = " + byteValue);
        System.out.println("This is a variable of type Short = " + shortValue);
        System.out.println("This is a variable of type Int = " + intValue);
        System.out.println("This is a variable of type Long = " + longValue);
        System.out.println("This is a variable of type Float = " + floatValue);
        System.out.println("This is a variable of type Double = " + doubleValue);
        System.out.println("This is a variable of type Character = " + charValue);
        System.out.println("This is a variable of type Boolean = " + booleanValue);
    }
}
